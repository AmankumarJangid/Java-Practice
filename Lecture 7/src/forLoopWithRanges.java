public class forLoopWithRanges {
    public static void main(String[] args) {
        //for each loop
        String[] names = new String[] {
                "Aman", "Faizal", "Alok", "Shivam" , "Aryan"
        };
        for( String name : names){
            System.out.println(name);
        }
    }
}
